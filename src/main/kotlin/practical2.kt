fun main(){
    var x: Int = 37
    println("Integer Value : $x")
    var y = x.toDouble()
    println("Double Value (From Integer) : $y")
    var a: String = "25"
    println("String Value : $a")
    var b = a.toInt()
    println("Integer Value1 : $b")
    var c: String = "10.22"
    println("String Value : $c")
    var d = c.toDouble()
    println("Double Value (From String) : $d")
}